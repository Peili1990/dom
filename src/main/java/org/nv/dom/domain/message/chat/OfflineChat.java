package org.nv.dom.domain.message.chat;

public class OfflineChat {
	
	/** 离线消息数量 */
	private Integer num;
	/** 对应会话 */
	private String chatId;
	
	public Integer getNum() {
		return num;
	}
	public void setNum(Integer num) {
		this.num = num;
	}
	public String getChatId() {
		return chatId;
	}
	public void setChatId(String chatId) {
		this.chatId = chatId;
	}
	

}
